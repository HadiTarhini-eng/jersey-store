export interface VisibleSocial extends SocialPlatform {
  href: string;
}

/**
 * The socials that should render right now: a platform shows only when it has
 * a URL configured AND its visibility key isn't explicitly `false`. Order
 * follows SOCIAL_PLATFORMS.
 */
export function getVisibleSocials(config: SiteConfig): VisibleSocial[] {
  const links   = config.socialLinks ?? {};
  const visible = config.socialLinksVisible ?? {};
  return SOCIAL_PLATFORMS
    .filter((p) => !!links[p.key]?.trim() && visible[p.key] !== false)
    .map((p) => ({ ...p, href: links[p.key]! }));
}
